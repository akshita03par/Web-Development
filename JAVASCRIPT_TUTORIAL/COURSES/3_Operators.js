let x = 10;
let y = 5;

// Arthmetic operators-----(+,-,*,/,%,**)

console.log(x+y);
console.log(x-y);
console.log(x*y);
console.log(x/y);
console.log(x%y);
console.log(x**y);

// unary operator------(++,--)

console.log(x++); //10
console.log(++x); //12


// Assignment operator------(=,+=,-=,/=,%= etc)
console.log('x is:',x);
console.log('y is:',y);
x+=y ;
console.log(x);

// comparison operators-----

let age = 21;
console.log(age>16); //true

console.log(age==21);//true
console.log(age!=21); // false

let str = '21'; // string
console.log(age==str); // true 

console.log(age===str); // false

// comparison for non numbers ---------

console.log('a'>'A'); // true
console.log('a'<'A'); //false



